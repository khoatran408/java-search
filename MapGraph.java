	/**
	* @param Additional method printGraph, return a path from start to goal follow parent map
	* @param  with key and value end at goal, begin from goal trace back to start, then reverse the path
	*/
	public List<GeographicPoint> printGraph(GeographicPoint start , GeographicPoint goal, Map<GeographicPoint, GeographicPoint> parent)
	{
		List<GeographicPoint> retMap = new LinkedList<GeographicPoint>();
		retMap.add(goal);
		GeographicPoint curr = parent.get(goal);
		retMap.add(curr);
		while (!start.equals(curr)){
			curr = parent.get(curr);
			retMap.add(curr);
		}
		Collections.reverse(retMap);
		return retMap;
	}
	
